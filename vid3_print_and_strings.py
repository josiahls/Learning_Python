"""
General intro to the print function
"""
# may or may not print Strings, example of strings
print('This is an example of print function')

# you can use single quotes, but double quotes might be needed
# for example the ' in we're
print("we're going to the store")

# this can be flipped around
print('He said "hi"')

# or you can use an escape character
print('we\'re going to the store')

"""
This is concating values
"""
# to concat you can do this:
print('hi' + 'there')
# or this
print('hi','there',5)
# but with ints you can do this
print('hi ' +str(5))
# and with floats
print(8.5 + 5 + float('6.7'))
